package class272.vo;

public class BoardNoticeVO {
	 private String noticeNo;		// 공지글번호
	 private String noticeName;		// 공지글제목
	 private String noticeTime;		// 공지글작성시간
	 private String noticeCon;		// 공지글내용
	 private String noticeDel;		// 공지글삭제
	
	 public String getNoticeNo() {
		return noticeNo;
	}
	public void setNoticeNo(String noticeNo) {
		this.noticeNo = noticeNo;
	}
	public String getNoticeName() {
		return noticeName;
	}
	public void setNoticeName(String noticeName) {
		this.noticeName = noticeName;
	}
	
	public String getNoticeTime() {
		return noticeTime;
	}
	public void String(String noticeTime) {
		this.noticeTime = noticeTime;
	}
	public String getNoticeCon() {
		return noticeCon;
	}
	public void setNoticeCon(String noticeCon) {
		this.noticeCon = noticeCon;
	}
	public String getNoticeDel() {
		return noticeDel;
	}
	public void setNoticeDel(String noticeDel) {
		this.noticeDel = noticeDel;
	}
	 
	 
}
