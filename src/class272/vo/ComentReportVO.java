package class272.vo;

public class ComentReportVO {
	 private String crNo;
	 private String crReason;
	 private String crDate;
	 private int comentNo;
	
	 
	 public String getCrNo() {
		return crNo;
	}
	public void setCrNo(String crNo) {
		this.crNo = crNo;
	}
	public String getCrReason() {
		return crReason;
	}
	public void setCrReason(String crReason) {
		this.crReason = crReason;
	}
	public String getCrDate() {
		return crDate;
	}
	public void setCrDate(String crDate) {
		this.crDate = crDate;
	}
	public int getComentNo() {
		return comentNo;
	}
	public void setComentNo(int comentNo) {
		this.comentNo = comentNo;
	}
	 
	 
}
